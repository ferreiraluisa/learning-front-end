//3 tipos de declarar variavel : var, let e const

var nomeVar = 'Luisa';
let nomeLet = 'Luisa';
const nomeConst = 'Luisa';

console.log(` nomeVar : ${nomeVar}`);
console.log(` nomeLet : ${nomeLet}`);
console.log(` nomeConst : ${nomeConst}`);

//let e const respeitam escopo de bloco, a var nao por isso se quiser declarar uma funcao para ficar apenas no escopo de bloco colocar let ou const